// 这是一个简单的文件系统实现的代码，按照五个层次组织：
//   + Blocks: allocator for raw disk blocks. 块：原始磁盘块的分配器。
//   + Log: crash recovery for multi-step updates. 日志：多步更新的崩溃恢复。
//   + Files: inode allocator, reading, writing, metadata. 文件：inode 分配器、读取、写入、元数据。
//   + Directories: inode with special contents (list of other inodes!) 目录：具有特殊内容的索引节点（其他索引节点的列表！）
//   + Names: paths like /usr/rtm/xv6/fs.c for convenient naming. 名称：类似 /usr/rtm/xv6/fs.c 的路径，以便于命名。

// 每个层次负责不同的功能，包括块分配、日志、文件管理、目录管理和文件名路径解析。

// 该文件包含低级文件系统操作例程。 （更高级别）系统调用实现在 sysfile.c 中。

